export function buildPayload(): string {
  return JSON.stringify({
    hookSpecificOutput: {
      hookEventName: "SessionStart",
      additionalContext: LEARNING_CONTEXT,
    },
  });
}

if (import.meta.main) {
  process.stdout.write(`${buildPayload()}\n`);
  process.exit(0);
}
